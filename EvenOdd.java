public class EvenOdd {
    public static void IsEvenOrOdd(int  n ) {
        if(n%2==0)
        System.out.println("Even");
        else
            System.out.println("Odd");
        
    }
    public static void main(String[] args) {
        int c=298;
        IsEvenOrOdd(c);
    }
    
}
